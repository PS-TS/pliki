public class Kalkulator {
    public static int dodawanie(int a , int b){
        return a+b;
    }
    public static int odejmowanie(int a, int b){
        return a-b;
    }

    public static int mnozenie(int a, int b){
        return a*b;
    }

    public static int dzielenie(int a, int b){
        return a/b;
    }
}
